package Java;

public class Class_and_Object {
	
	
	static int a,b,c;
	
	public static void add(int x, int y)
	{
		a=x;
		b=y;
		c=a+b;
		System.out.println("The addition result of c is "+c);
		
	}
	
	public void sub(int x, int y)
	{
		 a=x;
		 b=y;
		c=a-b;
		System.out.println("The subtraction result of c is "+c);
		
	}
   
	

}
